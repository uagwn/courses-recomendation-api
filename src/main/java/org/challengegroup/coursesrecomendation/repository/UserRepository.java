package org.challengegroup.coursesrecomendation.repository;

import org.challengegroup.coursesrecomendation.entity.User;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Transactional;

import java.time.LocalDateTime;
import java.util.Optional;

@Repository
public interface UserRepository extends JpaRepository<User, Long> {

    Optional<User> findByEmail(String email);
    boolean existsByEmail(String email);

    Optional<User> findByEmailAndIsActive(String email, Boolean isActive);

    @Modifying
    @Transactional
    @Query("UPDATE User u SET u.lastAccess = :now WHERE u.id = :id")
    void updateLastAccess(@Param("id") Long id, @Param("now") LocalDateTime now);
}
